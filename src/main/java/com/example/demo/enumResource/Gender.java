/**
 * The `Gender` enum represents different gender categories.
 * It provides a set of predefined constants for common gender categories.
 *
 * This enum defines the following gender categories:
 * - `Male`: Represents the male gender.
 * - `Female`: Represents the female gender.
 * - `Others`: Represents other or non-binary gender categories.
 *
 */
public enum Gender {
    Male,
    Female
}
