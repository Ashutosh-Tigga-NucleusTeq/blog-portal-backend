package com.blog.portal.enumResource;

/**
 * The `Status` enum represents different states or statuses that an entity or process can have.
 * It provides a set of predefined constants for common statuses.
 */
public enum Status {
    /**
     * Represents the status indicating that post has been approved or accepted.
     */
    APPROVED,

    /**
     * Represents the status indicating that post is waiting for a decision or action.
     */
    PENDING,

    /**
     * Represents the status indicating that post has been rejected or not accepted.
     */
    REJECTED
}
